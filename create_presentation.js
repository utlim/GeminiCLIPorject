const pptxgen = require("pptxgenjs");
const path = require("path");

let pres = new pptxgen();
pres.layout = 'LAYOUT_16x9'; // 와이드 스크린 16:9 규격 설정 (10인치 x 5.625인치)

const TITLE_FONT = "Gmarket Sans TTF Bold"; // 제목용 G마켓 산스 Bold
const BODY_FONT = "NanumGothic";          // 내용용 나눔고딕

// 노르딕 미니멀리즘 테마의 색상 팔레트 정의 (자연 친화적이고 차분한 톤)
const COLORS = {
  bgLight: "F4F1EC",      // 웜 크림 (Nordic Warm Cream - 슬라이드 기본 배경)
  bgDark: "3D3530",       // 다크 브라운 (Deep Charcoal Brown - 어두운 배경)
  cardBg: "FDFDFD",       // 소프트 화이트 (Soft Warm White - 카드 배경)
  cardBorder: "E5E1DA",   // 토프 그레이 (Taupe Grey - 테두리 및 얇은 라인)
  textPrimary: "3D3530",  // 주 텍스트 (Deep Brown - 묵직한 다크 브라운)
  textMuted: "7A6F62",    // 본문 텍스트 (Taupe - 따뜻하고 부드러운 그레이 브라운)
  textDarkBg: "EBE7E0",   // 어두운 배경용 본문 텍스트 (미색 회색)
  accentSage: "6B7F72",   // 세이지 그린 (Sage Green - 주요 차분한 포인트)
  accentSand: "C5B39D",   // 샌드 베이지 (Sand Beige - 보조 은은한 포인트)
  accentClay: "D29074"    // 클레이 테라코타 (Clay Terracotta - 악센트 포인트)
};

// 헬퍼 함수: 카드의 배경 상자를 그립니다. (노르딕 미니멀 둥근 카드 적용)
function drawCard(slide, x, y, w, h, fill, border) {
  slide.addShape(pres.shapes.ROUNDED_RECTANGLE, {
    x: x,
    y: y,
    w: w,
    h: h,
    fill: { color: fill || COLORS.cardBg },
    line: { color: border || COLORS.cardBorder, width: 1 },
    rectRadius: 0.08 // 조금 더 둥글고 부드러운 코너 적용
  });
}

// 헬퍼 함수: 카테고리 표시용 미니 태그를 그립니다. (차분한 세이지 그린 톤 적용)
function drawTag(slide, x, y, text, tagColor, textColor) {
  slide.addShape(pres.shapes.ROUNDED_RECTANGLE, {
    x: x, y: y, w: 1.3, h: 0.25,
    fill: { color: tagColor || COLORS.accentSage },
    line: { style: "none" },
    rectRadius: 0.2
  });
  slide.addText(text, {
    x: x, y: y, w: 1.3, h: 0.25,
    fontSize: 8.5, bold: true, fontFace: BODY_FONT,
    color: textColor || "FFFFFF", align: "center", valign: "middle"
  });
}

// 헬퍼 함수: 노르딕 미니멀 시그니처 3-dot 악센트를 그립니다.
function drawNordicDots(slide, x, y) {
  slide.addShape(pres.shapes.OVAL, { x: x, y: y, w: 0.08, h: 0.08, fill: { color: COLORS.accentSage }, line: { style: "none" } });
  slide.addShape(pres.shapes.OVAL, { x: x + 0.12, y: y, w: 0.08, h: 0.08, fill: { color: COLORS.accentSand }, line: { style: "none" } });
  slide.addShape(pres.shapes.OVAL, { x: x + 0.24, y: y, w: 0.08, h: 0.08, fill: { color: COLORS.accentClay }, line: { style: "none" } });
}

